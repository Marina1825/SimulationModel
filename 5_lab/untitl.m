% Пример массива битов (27000 битов)
msg_bits = randi([0, 1], 1, 27000); % Создаем массив из 27000 битов

% Удаляем первые 16 битов
msg_bits_without_first_16 = msg_bits(17:end);

% Проверка результата
disp('Исходный размер массива (в битах):');
disp(length(msg_bits));

disp('Размер массива после удаления первых 16 битов:');
disp(length(msg_bits_without_first_16));